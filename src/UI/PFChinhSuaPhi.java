    public void loadTable() {
        ArrayList<PhiDTO> list = PhiGuiXeDAO.loadPhi();
        DefaultTableModel tblModel = (DefaultTableModel) TableNhanVien.getModel();
        Object[] row = new Object[6];
        for (int i = tblModel.getRowCount() - 1; i >= 0; i--) {
            tblModel.removeRow(i);
        }
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getLoaiXe();
            row[1] = list.get(i).getPhiBanNgay();
            row[2] = list.get(i).getPhiBanDem();
            row[3] = list.get(i).getPhiThang();
            row[4] = list.get(i).getNgayTao();
            row[5] = list.get(i).getMaNV();
            tblModel.addRow(row);
        }
    }
