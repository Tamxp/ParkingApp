    private void btnThemTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTKActionPerformed

         try {
            String p1=BanNgay.getText();
            String p2=BanDem.getText();
            String p3=Thang.getText();
            if (BanNgay.getText().equals("")) {
                lbBanNgay.setText("*Chưa nhập phí ban ngày.");
                lbBanNgay.setForeground(Color.red);
                Border RedLine = BorderFactory.createLineBorder(Color.red);
                BanNgay.setBorder(RedLine);
            }
            else if (BanDem.getText().equals("")) {
                lbBanDem.setText("*Chưa nhập phí ban đêm.");
                lbBanDem.setForeground(Color.red);
                Border RedLine = BorderFactory.createLineBorder(Color.red);
                BanDem.setBorder(RedLine);
            }
            else if (Thang.getText().equals("")) {
                lbThang.setText("*Chưa nhập phí tháng.");
                lbThang.setForeground(Color.red);
                Border RedLine = BorderFactory.createLineBorder(Color.red);
                Thang.setBorder(RedLine);
            }
            else if (!BanNgay.getText().equals("") && !BanDem.getText().equals("") && !Thang.getText().equals("") ) {
                PhiGuiXeDAO pgx = new PhiGuiXeDAO();
                if (checkData(p1)==false){
                    lbBanNgay.setText("*Nhập phí không đúng định dạng");
                    lbBanNgay.setForeground(Color.red);
                    Border RedLine = BorderFactory.createLineBorder(Color.red);
                    BanNgay.setBorder(RedLine);
                }
                else if (checkData(p2)==false){
                    lbBanDem.setText("*Nhập phí không đúng định dạng");
                    lbBanDem.setForeground(Color.red);
                    Border RedLine = BorderFactory.createLineBorder(Color.red);
                    BanDem.setBorder(RedLine);
                }
                else if (checkData(p3)==false){
                    lbThang.setText("*Nhập phí không đúng định dạng");
                    lbThang.setForeground(Color.red);
                    Border RedLine = BorderFactory.createLineBorder(Color.red);
                    Thang.setBorder(RedLine);
                }
                else{
                    PhiDTO phi=new PhiDTO();
                    if (cbxLoaiXe3.getSelectedItem().equals("Xe máy")){
                        phi.setLoaiXe("Xe may");
                    }
                    else phi.setLoaiXe("Xe o to");
                    phi.setPhiBanNgay(Integer.parseInt(p1));
                    phi.setPhiBanDem(Integer.parseInt(p2));
                    phi.setPhiThang(Integer.parseInt(p3));
                    phi.setNgayTao(LocalDate.now());
                    phi.setMaNV(MaNV);
                    if (pgx.capNhatPhi(phi)> 0) {
                        JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                        BanNgay.setText("");
                        BanDem.setText("");
                        Thang.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Cập nhật không thành công!");
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemTKActionPerformed

