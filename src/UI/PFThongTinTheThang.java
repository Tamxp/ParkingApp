    private void btnChiTiet1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChiTiet1ActionPerformed
        // TODO add your handling code here:
        String maThe = jComboBoxMaThe.getSelectedItem().toString();
        if (jComboBoxMaThe.getSelectedItem()==null) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy thẻ tháng!");
        }
        else {
                int response = JOptionPane.showConfirmDialog(
                    null,
                    "Bạn có chắc chắn muốn hủy thẻ "+maThe+ " ?",
                    "Confirm",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE
                );

                // Xử lý phản hồi từ người dùng
                if (response == JOptionPane.YES_OPTION) {
                    if(DangKyVeThangDAO.updateVethang(maThe)>0){
                        JOptionPane.showMessageDialog(this, "Hủy thẻ thành công!");
                        loadMaThe(ma);
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Không tìm thấy thẻ tháng!");
                    }
                } else if (response == JOptionPane.NO_OPTION) {
                    
                }
        }
    }//GEN-LAST:event_btnChiTiet1ActionPerformed


